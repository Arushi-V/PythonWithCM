# Python Variables - Assign Multiple Values
# normal approach
a = 10
b = 20
c = 30
print(a, b, c)

# multiple values approach
a, b, c = 100, 200, 300
print(a, b, c)

# One Value to Multiple Variables
p = r = t = 0

# Unpack a Collection
person = ["name", "arushi"]
key, value = person
print(key)
print(value)